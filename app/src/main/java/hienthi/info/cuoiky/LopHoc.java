package hienthi.info.cuoiky;

public class LopHoc {
    public String maL;
    public String tenL;
    public int siso;
    public String gvcn;
    public String gvtoan;
    public String gvly;
    public String gvhoa;
    public byte[] anhL;

    public LopHoc(String maL, String tenL, int siso, String gvcn, String gvtoan, String gvly, String gvhoa, byte[] anhL) {
        this.maL = maL;
        this.tenL = tenL;
        this.siso = siso;
        this.gvcn = gvcn;
        this.gvtoan = gvtoan;
        this.gvly = gvly;
        this.gvhoa = gvhoa;
        this.anhL = anhL;
    }
}
